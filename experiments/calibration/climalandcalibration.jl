struct ClimaLandCalibration
    landmodel # bucket, full land, soil ...
    scale # single_site(site_ID), global, regional, ... - only global for now
    variable_list # ["swu"], ["lhf", "shf"], ...
    n_iterations # 10, 20, ...
    spinup_period # Year(1), Month(3), ...
    calibration_period # only Year(1) for now
    start_date # DateTime(2008) - right now, always December 1st
    prior_path
    model_spatial_resolution # (50, 10), (25, 5), ...
    observation_temporal_resolution # only :seasonal for now, could add :month
    output_dir_path
    locations # only :land_all for now, could be lat or lon bands, snow locations, etc.
    target_observation # perfect_model, era5, other...
    noise # could be flat(5, 10, 20) - same length as variable_list, :obs_variance, obs_mean(5) - 5% ...
    ekp_config # tuple of kwargs ekp config
    climacalibrate_backend # WorkerBackend, JuliaBackend, ...
end
