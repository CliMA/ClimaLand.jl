const N_PARAMS = length(PARAM_NAMES)

"""Write a parameter override TOML file."""
function write_override_toml(path, param_names, param_values)
    open(path, "w") do io
        for (name, val) in zip(param_names, Float64.(param_values))
            println(io, "[\"$(name)\"]")
            println(io, "value = $(val)")
            println(io, "type  = \"float\"")
            println(io)
        end
    end
end

"""
    run_model_year(params_vec, year) -> Vector{Float64}

Run a single-year ClimaLand simulation for DK-Sor and return monthly means
[NEE_jan..dec (gC/m²/d), Qle_jan..dec (W/m²), Qh_jan..dec (W/m²)] = 36 entries.
This matches the obs vector layout from generate_observations.jl exactly.
"""
function run_model_calib(params_vec, calib_years)
    # Continuous FULL-BATCH forward model: cold-start a 1-yr spin-up
    # (calib_years[1]-1, discarded) then run calib_years CONTINUOUSLY, returning
    # per-year monthly means, year-major: [yr1 NEE×12, LHF×12, SHF×12, yr2 …].
    # Matches the 1-yr calibration spin-up AND the continuous spun-up output,
    # and gives a constant G dim so CES needs no minibatch flattening.
    N_OUT = 36 * length(calib_years)

    if any(isnan, params_vec)
        @warn "run_model_calib: NaN in params_vec, skipping"
        return fill(NaN, N_OUT)
    end

    spinup_year = calib_years[1] - 1                  # 1-yr spin-up, discarded
    start_date  = DateTime(spinup_year, 1, 1)
    stop_date   = DateTime(calib_years[end] + 1, 1, 1)

    # Split params: TOML-backed (written to an override) vs site/model params
    # (e.g. α_NIR_leaf) that are routed directly into the model below.
    param_map  = Dict(zip(PARAM_NAMES, params_vec))
    toml_idx   = findall(n -> !(n in NON_TOML_PARAMS), PARAM_NAMES)
    tmpfile = tempname() * ".toml"
    write_override_toml(tmpfile, PARAM_NAMES[toml_idx], params_vec[toml_idx])

    try
        local_toml = LP.create_toml_dict(FT; override_files = [tmpfile])

        (; dz_tuple, nelements, zmin, zmax) =
            FluxnetSimulations.get_domain_info(FT, Val(SITE_ID_VAL))
        land_domain = Column(;
            zlim      = (FT(zmin), FT(zmax)),
            nelements, dz_tuple,
            longlat   = (long, lat),
        )
        canopy_domain = ClimaLand.Domains.obtain_surface_domain(land_domain)

        forcing = FluxnetSimulations.prescribed_forcing_netcdf(
            MET_NC_PATH, lat, long, time_offset, atmos_h,
            start_date, local_toml, FT,
        )

        # LAI from met file — LAI_alternative is MODIS-derived
        LAI, maxLAI = NCDataset(MET_NC_PATH, "r") do ds
            time_vals = ds["time"][:]
            lai_data  = Float64.(coalesce.(ds["LAI_alternative"][1, 1, :], NaN))
            lai_secs  = Float64[
                Second(t - Hour(time_offset) - start_date).value
                for t in time_vals
            ]
            valid = .!isnan.(lai_data)
            TimeVaryingInput(lai_secs[valid], lai_data[valid]), maximum(lai_data[valid])
        end

        (; soil_ν, θ_r, soil_K_sat, soil_vg_n, soil_vg_α,
           ν_ss_om, ν_ss_quartz, ν_ss_gravel,
           z_0m_soil, z_0b_soil, soil_α_PAR, soil_α_NIR, soil_ϵ,
           Ω, χl, α_PAR_leaf, τ_PAR_leaf, α_NIR_leaf, τ_NIR_leaf,
           ϵ_canopy, ac_canopy, Drel, g0,
           SAI, f_root_to_shoot, rooting_depth, h_canopy,
           conductivity_model, retention_model, plant_ν, plant_S_s) = _SITE_PARAMS

        # Route non-TOML calibrated params directly into the model (site params).
        haskey(param_map, "α_NIR_leaf") && (α_NIR_leaf = FT(param_map["α_NIR_leaf"]))

        RAI = maxLAI * f_root_to_shoot

        prognostic_land_components = (:canopy, :snow, :soil, :soilco2)

        soil_albedo = Soil.ConstantTwoBandSoilAlbedo{FT}(;
            PAR_albedo = soil_α_PAR, NIR_albedo = soil_α_NIR)
        retention_parameters = (;
            ν      = soil_ν,
            θ_r,
            K_sat  = soil_K_sat,
            hydrology_cm = vanGenuchten{FT}(; α = soil_vg_α, n = soil_vg_n),
        )
        composition_parameters = (; ν_ss_om, ν_ss_quartz, ν_ss_gravel)

        soil = Soil.EnergyHydrology{FT}(
            land_domain, forcing, local_toml;
            prognostic_land_components,
            additional_sources   = (ClimaLand.RootExtraction{FT}(),),
            albedo               = soil_albedo,
            runoff               = ClimaLand.Soil.Runoff.SurfaceRunoff(),
            retention_parameters, composition_parameters,
            S_s = FT(1e-3), z_0m = z_0m_soil, z_0b = z_0b_soil,
        )

        co2_drivers = Soil.Biogeochemistry.SoilDrivers(
            Soil.Biogeochemistry.PrognosticMet(soil.parameters), forcing.atmos,
        )
        soilco2 = Soil.Biogeochemistry.SoilCO2Model{FT}(
            land_domain, co2_drivers, local_toml,
        )

        radiation_parameters = (;
            Ω, G_Function = CLMGFunction(χl),
            α_PAR_leaf, τ_PAR_leaf, α_NIR_leaf, τ_NIR_leaf,
        )
        radiative_transfer = Canopy.TwoStreamModel{FT}(
            canopy_domain, local_toml; radiation_parameters, ϵ_canopy,
        )

        biomass = Canopy.PrescribedBiomassModel{FT}(;
            LAI, SAI, RAI, rooting_depth, height = h_canopy,
        )
        canopy = Canopy.CanopyModel{FT}(
            canopy_domain,
            (; atmos = forcing.atmos, radiation = forcing.radiation,
               ground = ClimaLand.PrognosticGroundConditions{FT}()),
            LAI, local_toml;
            prognostic_land_components,
            radiative_transfer,
            photosynthesis       = Canopy.PModel{FT}(canopy_domain, local_toml),
            conductance          = Canopy.PModelConductance{FT}(local_toml; Drel),
            soil_moisture_stress = Canopy.PiecewiseMoistureStressModel{FT}(
                land_domain, local_toml; soil_params = (; ν = soil_ν, θ_r)),
            hydraulics = Canopy.PlantHydraulicsModel{FT}(
                canopy_domain, local_toml;
                ν = plant_ν, S_s = plant_S_s, conductivity_model, retention_model),
            energy  = Canopy.BigLeafEnergyModel{FT}(local_toml; ac_canopy),
            biomass,
        )
        snow = Snow.SnowModel(
            FT, canopy_domain, forcing, local_toml, DT;
            prognostic_land_components,
        )
        land = LandModel{FT}(canopy, snow, soil, soilco2, nothing)

        # ── Initial conditions ────────────────────────────────────────────────
        T_init_K = NCDataset(MET_NC_PATH, "r") do ds
            t_dates = DateTime.(ds["time"][:])
            idx_yr  = findfirst(t -> Dates.year(t) == spinup_year, t_dates)
            isnothing(idx_yr) ? 283.15 : Float64(coalesce(ds["Tair"][1, 1, idx_yr], 283.15))
        end

        function set_ic!(Y, p, t, model)
            FT_l = eltype(Y.soil.ρe_int)
            ν_l  = FT_l(soil_ν)
            θr_l = FT_l(θ_r)
            Y.soil.ϑ_l .= θr_l + (ν_l - θr_l) * FT_l(0.95)
            Y.soil.θ_i .= FT_l(0)
            ρc_s = ClimaLand.Soil.volumetric_heat_capacity.(
                Y.soil.ϑ_l, Y.soil.θ_i,
                soil.parameters.ρc_ds, soil.parameters.earth_param_set)
            Y.soil.ρe_int .= ClimaLand.Soil.volumetric_internal_energy.(
                Y.soil.θ_i, ρc_s, FT_l(T_init_K),
                soil.parameters.earth_param_set)
            Y.snow.S .= FT_l(0); Y.snow.S_l .= FT_l(0); Y.snow.U .= FT_l(0)
            Y.canopy.energy.T .= FT_l(T_init_K)
            Y.canopy.hydraulics.ϑ_l .= model.canopy.hydraulics.parameters.ν
            # SoilCO2 ICs — prognostic_vars = (:CO2, :O2, :SOC)
            Y.soilco2.CO2 .= FT_l(6e-5)      # kg C/m³
            Y.soilco2.O2  .= FT_l(0.21)      # volumetric fraction (~atmospheric)
            SOC_top = FT_l(15.0); SOC_bot = FT_l(0.5)
            τ_soc   = FT_l(1.0 / log(SOC_top / SOC_bot))
            z = ClimaCore.Fields.coordinate_field(axes(Y.soilco2.SOC)).z
            @. Y.soilco2.SOC = SOC_bot + (SOC_top - SOC_bot) * exp(z / τ_soc)
        end

        # ── Diagnostics: daily averages, aggregated to monthly in extraction ─────
        diags = ClimaLand.default_diagnostics(
            land, start_date, "";
            output_writer    = ClimaDiagnostics.Writers.DictWriter(),
            output_vars      = ["lhf", "shf", "nee"],
            reduction_period = :daily,
        )
        simulation = LandSimulation(
            start_date, stop_date, DT, land;
            set_ic!, updateat = Second(DT), diagnostics = diags,
        )

        # Timing instrumentation (flushed to file immediately — slurm stderr buffers,
        # so a hang in solve! is otherwise invisible). Gated off in production by
        # leaving CALLMIP_TIMING_LOG unset.
        tlog = get(ENV, "CALLMIP_TIMING_LOG", "")
        n_my = length(spinup_year:calib_years[end])
        if !isempty(tlog)
            open(tlog, "a") do io
                println(io, "$(round(time();digits=1)) w$(myid()) START solve ($(n_my) model-yrs: $(spinup_year)→$(calib_years[end]))"); flush(io)
            end
        end
        t_solve = @elapsed Logging.with_logger(SimpleLogger(devnull, Logging.Error)) do
            solve!(simulation)
        end
        if !isempty(tlog)
            open(tlog, "a") do io
                println(io, "$(round(time();digits=1)) w$(myid()) DONE  solve in $(round(t_solve;digits=1))s = $(round(t_solve/60/n_my;digits=2)) min/model-yr"); flush(io)
            end
        end

        # ── Extract G: per-(year,month) all-day means for the calib years ────────
        # (the spin-up year calib_years[1]-1 is simply absent from the loop). G is
        # year-major to match the stacked observation: [yr NEE×12, LHF×12, SHF×12].
        writer = simulation.diagnostics[1].output_writer
        function monthly_year(diag_name, y, scale = 1.0)
            times, data = ClimaLand.Diagnostics.diagnostic_as_vectors(writer, diag_name)
            ddates = Date.(date.(times))
            yrs = Dates.year.(ddates); mons = Dates.month.(ddates)
            [ (sel = (yrs .== y) .& (mons .== m); any(sel) ? mean(data[sel]) * scale : NaN)
              for m in 1:12 ]
        end
        g_vec = Float64[]
        for y in calib_years
            append!(g_vec, monthly_year("nee_1d_average", y, 12.0 * 86400.0))  # → gC/m²/d
            append!(g_vec, monthly_year("lhf_1d_average", y))
            append!(g_vec, monthly_year("shf_1d_average", y))
        end
        n_nan = count(isnan, g_vec)
        n_nan > 0 && @warn "run_model_calib: $n_nan NaN in G on worker $(myid())"
        return g_vec

    catch e
        @warn "run_model_calib failed on worker $(myid()): $(typeof(e)): $e"
        return fill(NaN, N_OUT)
    finally
        rm(tmpfile; force = true)
    end
end

"""Full-batch continuous forward map: one 36×n_years G vector (year-major)."""
G_func(params_vec, years) = run_model_calib(params_vec, years)

end  # @everywhere

# ── Master-only: EKI setup and calibration loop ───────────────────────────────
import EnsembleKalmanProcesses as EKP
import EnsembleKalmanProcesses.ParameterDistributions as PD
using JLD2
using Random
using Statistics
using LinearAlgebra

const CLIMALAND_DIR   = pkgdir(ClimaLand)
const OUTDIR          = get(ENV, "CALLMIP_OUTDIR", joinpath(CLIMALAND_DIR,
    "experiments/callmip_dksor/output_calibration"))
const OBS_PATH        = joinpath(OUTDIR, "observations.jld2")
const CHECKPOINT_PATH = joinpath(OUTDIR, "ekp_checkpoint.jld2")
mkpath(OUTDIR)

include(joinpath(@__DIR__, "priors.jl"))

# ── Load observations ─────────────────────────────────────────────────────────
obs_data   = jldopen(OBS_PATH, "r") do f
    (; calib_years = f["calib_years"], obs_series = f["obs_series"],
       valid_dates_series = f["valid_dates_series"])
end
calib_years = obs_data.calib_years
obs_series  = obs_data.obs_series
# year → per-flux valid-obs-day Dict; threaded into G(θ) for model-side day matching
vdays_by_year = Dict(calib_years[i] => obs_data.valid_dates_series[i]
                     for i in eachindex(calib_years))

@info "Loaded $(length(obs_series)) observation windows: $(calib_years)"

# ── Build prior and initial ensemble ─────────────────────────────────────────
prior, _ = build_dk_sor_priors()

rng = Random.MersenneTwister(42)

# ── Build ObservationSeries: BIENNIAL minibatches (HYBRID) ────────────────────
# Group the yearly windows into CONSECUTIVE pairs ("biennial units"); each unit is
# one minibatch and is run continuously by run_model_calib with ONE shared 1-yr
# spin-up (spin up unit[1]-1, run the 2 obs years) — i.e. "spin up 1 year before
# the 2-year minibatch". This is ~4× cheaper per iteration than the full 1+11-year
# continuous batch, and is the SAME shape as the eventual global pipeline.
#   • calibration: minibatch (variable-dim G across iters is fine for UTKI updates)
#   • CES:         a separate constant-dim full-obs pass (below) feeds the emulator,
#                  since get_training_points needs a fixed G dim. Realistic obs noise
#                  (generate_observations.jl) is what fixes the coverage, not batching.
# Consecutive grouping (not RandomFixedSizeMinibatcher, which gives non-adjacent
# pairs the continuous forward model can't run): indices 1:2,3:4,…; last may be 1.
group_idx   = [collect(i:min(i + 1, length(calib_years))) for i in 1:2:length(calib_years)]
# UTKI sizes its internal structure from observations[1] and requires a UNIFORM
# observation dimension across minibatches. An odd #years leaves a trailing
# singleton (36-dim) that breaks the 72-dim biennial layout → BoundsError in
# update_ensemble!. Drop it (the year is still used in the CES obs + propagation).
if length(group_idx[end]) == 1
    dropped = calib_years[group_idx[end][1]]
    @warn "Dropping trailing singleton calibration group (year $dropped) so all minibatches are biennial (72-dim)"
    pop!(group_idx)
end
group_years = [calib_years[g] for g in group_idx]
group_obs   = [EKP.combine_observations(obs_series[g]) for g in group_idx]   # all 72-dim
# One biennial unit per iteration (size-1 minibatch), reshuffled each epoch.
obs_series_ekp = EKP.ObservationSeries(group_obs, EKP.RandomFixedSizeMinibatcher(1))
@info "Biennial minibatch groups ($(length(group_obs)), each $(length(group_years[1])) yr): $(group_years)"

# Full 396-dim observation (all years stacked) — used ONLY for the CES training pass
# and CES/MCMC conditioning, never for the minibatched calibration updates.
combined_obs_full = EKP.combine_observations(obs_series)

# ── EKP object ────────────────────────────────────────────────────────────────
# Use Unscented Transform Kalman Inversion (UTKI), matching the official ClimaLand
# pipeline (experiments/calibration/run_calibration.jl). UTKI auto-sizes the
# ensemble to 2p+1 (no N_ens tuning / localization needed), is deterministic, and
# `impose_prior=true` regularizes toward the prior. The update loop is unchanged.
function build_ekp(prior, obs_series_ekp, rng)
    EKP.EnsembleKalmanProcess(
        obs_series_ekp,
        EKP.TransformUnscented(prior; impose_prior = true);
        scheduler = EKP.DataMisfitController(terminate_at = 100),
        verbose = true,
        rng,
    )
end

# ── Checkpoint load/save ──────────────────────────────────────────────────────
function save_checkpoint(ekp, iteration)
    jldsave(CHECKPOINT_PATH; ekp, iteration)
    @info "  Checkpoint saved (iteration $iteration)"
end

function load_checkpoint()
    isfile(CHECKPOINT_PATH) || return nothing, 0
    d = JLD2.load(CHECKPOINT_PATH)
    @info "Resuming from checkpoint: iteration $(d["iteration"])"
    return d["ekp"], d["iteration"]
end

# ── Forward model runner (master dispatches to workers) ───────────────────────
# `years` is the current minibatch's biennial unit (consecutive), passed as a typed
# local so the pmap closure stays type-stable (capturing a non-const global forces
# per-member recompilation — a ~3-7x slowdown).
function run_ensemble(ekp, years)
    ϕ_ens = EKP.get_ϕ_final(prior, ekp)   # N_params × N_ens
    @info "  Minibatch years: $(years) ($(length(years)) yr + 1 spin-up)"

    if IS_TEST || nworkers() == 0
        G_ens = hcat([G_func(ϕ_ens[:, m], years)
                      for m in 1:size(ϕ_ens, 2)]...)
    else
        G_ens = hcat(pmap(m -> G_func(ϕ_ens[:, m], years),
                         1:size(ϕ_ens, 2))...)
    end
    return G_ens
end

# ── CES training pass: re-evaluate the converged param cloud on the FULL obs ──
# Minibatched (u,G) pairs have variable G dim, which CES get_training_points cannot
# stack. So gather the last few iterations' ensembles (the converged cloud) and
# evaluate each on ALL years continuously (constant 396-dim G), then save the
# (u_unconstrained, G_full) pairs + the full observation for emulate_sample.jl.
# This is a temporary stand-in until upstream minibatch→CES support lands; with it the
# minibatched pairs feed CES directly and this pass disappears.
function run_ces_training_pass(ekp, prior, calib_years, combined_obs_full)
    n_done = length(EKP.get_u(ekp)) - 1                 # completed iterations
    k      = min(n_done, parse(Int, get(ENV, "CALLMIP_CES_ITERS", "3")))
    iters  = (n_done - k + 1):n_done
    u_unc  = reduce(hcat, [EKP.get_u(ekp, i) for i in iters])   # N_params × (k·N_ens)
    ϕ_con  = PD.transform_unconstrained_to_constrained(prior, u_unc)
    years  = sort(calib_years)
    @info "  CES training pass: $(size(u_unc,2)) param vectors × $(length(years)) yr on full obs"
    if nworkers() <= 1
        G_full = hcat([G_func(ϕ_con[:, m], years) for m in 1:size(ϕ_con, 2)]...)
    else
        G_full = hcat(pmap(m -> G_func(ϕ_con[:, m], years), 1:size(ϕ_con, 2))...)
    end
    return u_unc, G_full
end

# ── Per-flux RMSE per iteration (diagnostic) ──────────────────────────────────
# Evaluate each iteration's MEAN parameters on the FULL observation (constant dim)
# and compute per-flux RMSE (NEE/LHF/SHF) vs obs — a fixed-data convergence curve.
# Works on any ekp (fresh or checkpoint-loaded; iterates its stored states).
function compute_per_flux_rmse(ekp, prior, calib_years, combined_obs_full)
    years    = sort(calib_years)
    n_states = length(EKP.get_u(ekp))                     # iters+1 (incl. initial)
    obs = EKP.get_obs(combined_obs_full)
    sig = sqrt.(diag(EKP.get_obs_noise_cov(combined_obs_full)))
    ny  = length(years)
    # year-major idx = (y-1)*36 + (f-1)*12 + m; valid = unmasked (sig < 1e11)
    fidx = [Int[] for _ in 1:3]
    for y in 1:ny, f in 1:3, m in 1:12
        idx = (y - 1) * 36 + (f - 1) * 12 + m
        sig[idx] < 1e5 && push!(fidx[f], idx)   # unmasked (σ=√1e12=1e6 for missing)
    end
    means = [PD.transform_unconstrained_to_constrained(
                 prior, vec(mean(EKP.get_u(ekp, i); dims = 2))) for i in 1:n_states]
    @info "  Per-flux RMSE: evaluating $(n_states) iteration-means on full obs"
    Gs = nworkers() <= 1 ? [G_func(m, years) for m in means] :
                            pmap(m -> G_func(m, years), means)
    rmse = fill(NaN, n_states, 3)
    for (it, G) in enumerate(Gs), f in 1:3
        d = G[fidx[f]] .- obs[fidx[f]]
        dd = d[isfinite.(d)]
        isempty(dd) || (rmse[it, f] = sqrt(mean(dd .^ 2)))
    end
    return rmse                                           # n_states × 3 (NEE,LHF,SHF)
end

# ── Calibration loop ──────────────────────────────────────────────────────────
ekp, start_iter = load_checkpoint()
if isnothing(ekp)
    ekp = build_ekp(prior, obs_series_ekp, rng)
    start_iter = 0
    @info "UTKI ensemble size (2p+1) = $(EKP.get_N_ens(ekp)) for $(N_PARAMS) parameters"
end

# Diagnostic-only mode: skip the calibration loop, just compute the per-flux RMSE
# convergence curve from an existing checkpoint and exit (e.g. for a finished run).
if get(ENV, "CALLMIP_RMSE_ONLY", "0") == "1"
    rmse = compute_per_flux_rmse(ekp, prior, calib_years, combined_obs_full)
    jldsave(joinpath(OUTDIR, "calibration_rmse_perflux.jld2"); rmse, fluxes = ["NEE", "LHF", "SHF"])
    for it in 1:size(rmse, 1)
        @info "  iter $(it-1): NEE=$(round(rmse[it,1];sigdigits=3)) LHF=$(round(rmse[it,2];sigdigits=3)) SHF=$(round(rmse[it,3];sigdigits=3))"
    end
    @info "Per-flux RMSE saved → $(joinpath(OUTDIR, "calibration_rmse_perflux.jld2"))"
    exit(0)
end

for iter in (start_iter + 1):N_ITERATIONS
    @info "Iteration $iter / $N_ITERATIONS"
    # Active biennial unit for this iteration (consecutive years → continuous run).
    mb    = EKP.get_current_minibatch(ekp)
    years = reduce(vcat, group_years[mb])
    G_ens = run_ensemble(ekp, years)

    # Mark failed forward runs as NaN (Inf → NaN too) and hand the raw matrix to
    # EKP. Its SampleSuccGauss failure handler detects NaN members (nan_tolerance)
    # and updates them from the successful ensemble — the correct way to ignore
    # crashes. We do NOT replace failures with a penalty value.
    G_ens[.!isfinite.(G_ens)] .= NaN
    failed_cols = findall(j -> any(isnan, G_ens[:, j]), 1:size(G_ens, 2))
    @info "  G_ens: $(size(G_ens)), failed (NaN) members: $(length(failed_cols))"

    EKP.update_ensemble!(ekp, G_ens)
    save_checkpoint(ekp, iter)

    ϕ_mean_iter = EKP.get_ϕ_mean_final(prior, ekp)
    @info "  Posterior means:"
    for (name, val) in zip(PARAM_NAMES, ϕ_mean_iter)
        @info "    $(rpad(name, 35)) = $(round(val; sigdigits=4))"
    end
end

# ── Save posterior ensemble ───────────────────────────────────────────────────
ϕ_final      = EKP.get_ϕ_final(prior, ekp)
ϕ_mean_final = EKP.get_ϕ_mean_final(prior, ekp)

@info "Posterior ensemble mean:"
for (i, name) in enumerate(PARAM_NAMES)
    @info "  $name = $(round(ϕ_mean_final[i]; sigdigits=4))"
end

post_path = joinpath(OUTDIR, "posterior_ensemble.jld2")
jldsave(post_path;
    param_names   = PARAM_NAMES,
    ϕ_ensemble    = ϕ_final,
    ϕ_mean        = ϕ_mean_final,
    ekp_checkpoint = CHECKPOINT_PATH,
)
@info "Posterior ensemble saved → $post_path"

# ── CES training pass + save ──────────────────────────────────────────────────
# Skip with CALLMIP_SKIP_CES_PASS=1 (e.g. fast calibration-only test).
if get(ENV, "CALLMIP_SKIP_CES_PASS", "0") != "1"
    u_train, G_train = run_ces_training_pass(ekp, prior, calib_years, combined_obs_full)
    u_mean_final = EKP.get_u_mean(ekp, length(EKP.get_u(ekp)) - 1)  # unconstr. posterior mean (MCMC init)
    ces_path = joinpath(OUTDIR, "ces_training.jld2")
    jldsave(ces_path;
        u_train,                      # N_params × N_pts (unconstrained)
        G_train,                      # (36·n_years) × N_pts  (full-obs, constant dim)
        u_mean_final,
        combined_obs = combined_obs_full,
        param_names  = PARAM_NAMES,
        calib_years,
    )
    n_fin = count(j -> all(isfinite, G_train[:, j]), 1:size(G_train, 2))
    @info "CES training data saved → $ces_path  ($(size(G_train,1))-dim G, $n_fin/$(size(G_train,2)) finite cols)"
end

