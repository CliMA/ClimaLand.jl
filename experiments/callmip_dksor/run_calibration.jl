"""
EKI calibration for DK-Sor CalLMIP Phase 1a.

Runs EnsembleKalmanInversion (30 members, up to 10 iterations) to calibrate
15 ClimaLand parameters against daily NEE, Qle, Qh observations (2003–2013).
Parallelises forward-model evaluations across Julia workers using pmap.

Pipeline:
  1. Load observations from output_calibration/observations.jld2
     (generated by generate_observations.jl)
  2. Build EKP.ObservationSeries with RandomFixedSizeMinibatcher(2)
  3. Run EKI until DataMisfitController(terminate_at=100) stops or N_ITER reached
  4. Save posterior ensemble to output_calibration/posterior_ensemble.jld2

Each worker runs `run_model_year(params, year)`:
  - Single-year ClimaLand simulation, DT=900 s
  - Parameters injected via TOML override file
  - Returns G-vector: [NEE×12×86400, Qle, Qh] daily, length = n_days_in_year × 3
  - Missing model days filled with 0.0 (obs noise = 1e12 handles mismatch)

Test gate (before submitting to SLURM):
  julia --project=experiments/callmip_dksor \
        experiments/callmip_dksor/run_calibration.jl --test
  Runs 1 iteration with 2 members (serial), checks G vector is finite.

Full run:
  julia -p 30 --project=.buildkite \
        experiments/callmip_dksor/run_calibration.jl
"""

# ── Distributed setup (must precede all imports) ──────────────────────────────
using Distributed

const IS_TEST = "--test" in ARGS   # 1 iteration, 2 members, 1-year minibatch, serial

if IS_TEST
    const N_ENS        = 2
    const N_ITERATIONS = 1
    @info "Test mode: N_ENS=$N_ENS, N_ITERATIONS=$N_ITERATIONS (serial)"
else
    # Only call addprocs if workers not already provided by `julia -p N`
    if nworkers() == 1
        const N_WORKERS = max(1,
            parse(Int, get(ENV, "SLURM_NTASKS", string(Sys.CPU_THREADS))) - 1)
        addprocs(N_WORKERS; exeflags = "--project=$(Base.active_project())")
    end
    @info "Using $(nworkers()) Julia workers"
    const N_ENS        = 30
    const N_ITERATIONS = 10
end

@everywhere import ClimaComms
@everywhere ClimaComms.@import_required_backends

